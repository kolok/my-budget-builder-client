<template>
  <div>
    <template>
      <el-button
        type="primary"
        @click="createDialog = true"
      >
        Create a new entity
      </el-button>
    </template>

    <el-dialog
      title="Create an entity"
      :visible.sync="createDialog"
    >
      <el-form
        ref="entityForm"
        :model="entityForm"
        :rules="entityRule"
        label-width="250px"
        class="Dialog__Form"
      >
        <el-form-item
          prop="name"
          label="Entity"
        >
          <el-input
            v-model="entityForm.name"
            autocomplete="off"
          />
        </el-form-item>
        <el-form-item
          label="Country"
          prop="countryID"
        >
          <el-select
            v-model="entityForm.countryID"
            placeholder="Select a country"
          >
            <el-option
              v-for="country in countries"
              :key="country.id"
              :label="country.name"
              :value="country.id"
            />
          </el-select>
        </el-form-item>
        <el-form-item
          label="Currency"
          prop="defaultCurrencyID"
        >
          <el-select
            v-model="entityForm.defaultCurrencyID"
            placeholder="Select a currency"
          >
            <el-option
              v-for="currency in currencies"
              :key="currency.id"
              :label="currency.longName"
              :value="currency.id"
            />
          </el-select>
        </el-form-item>
      </el-form>
      <span
        slot="footer"
      >
        <el-button @click="handleCancel">Cancel</el-button>
        <el-button
          type="primary"
          @click="handleCreateEntity('entityForm')"
        >Save</el-button>
      </span>
    </el-dialog>
  </div>
</template>

<script>
import { mapGetters, mapActions } from 'vuex'

export default {
  data() {
    return {
      currencyList: [],
      createDialog: false,
      entityForm: {
        name: '',
        countryID:'',
        defaultCurrencyID: ''
      },
      entityRule: {
        name: [
          { required: true, message: 'Entity name can\'t be blank' },
          { max:25, message: 'Too long'},
          { min:3, message: 'Too short'}
        ],
        countryID: [
          { required: true, message: 'A country should be selected' }
        ],
        defaultCurrencyID: [
          { required: true, message: 'A currency should be selected' }
        ]
      }
    }
  },
  computed: {
    ...mapGetters(['currencies', 'countries'])
  },
  methods: {
    ...mapActions(['createEntity']),
    /*eslint no-unused-vars: ["error", { "args": "none" }]*/
    handleCreateEntity: function(formName) { // Create entity
      this.$refs[formName].validate((valid) => {
        if (valid) {
          this.createEntity(this.entityForm)
            .then(response => {
              // reset form data
              this.$refs[formName].resetFields()
            })
            .catch(e => {
              console.log(e)
            })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    },
    handleCancel: function(){
      this.createDialog = false
    }
  }
}
</script>
